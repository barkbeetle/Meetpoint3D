<div class="title">Welcome to Meetpoint3D</div>
<p>
	Meetpoint 3D is a test project to evaluate WebGL, a new web standard for hardware accelerated 3D graphics within web browsers.<br/>
	In a first version, the user will be able to control an avatar across a virtual 3-dimensional world and chat with other real players. Additional features may be added to the software later.
</p>
<div class="title2">WebGL</div>
<p>
	WebGL is a Web-based Graphics Library. It extends the capability of the JavaScript programming language to allow it to generate interactive 3D graphics within any compatible web browser.<br/>
	WebGL is a context of the canvas HTML element that provides a 3D computer graphics API without the use of plug-ins. The specification was released as version 1.0 on March 3, 2011. WebGL is managed by the non-profit Khronos Group.
